#include <strace.h>

////////////////////////////////////////////////////////////////////////////////
/// STATIC FUNCTION
////////////////////////////////////////////////////////////////////////////////

static int usage(void)
{
	fprintf(stderr, "strace: usage: ./ft_strace <file>\n");

	return FAILURE;
}

////////////////////////////////////////////////////////////////////////////////
/// BOOTSTRAP FUNCTION
////////////////////////////////////////////////////////////////////////////////

int main(int argc, char **argv)
{
	if (argc != 2)
		return usage();

	return strace(argv[1]);
}
